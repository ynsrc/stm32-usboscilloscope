/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

	// Calibrate ADC (Important! It must be every start to provide certainity)
	if (HAL_ADCEx_Calibration_Start(&hadc1) != HAL_OK)
	{
		Error_Handler();
	}

	// Start ADC with DMA Mode
	// Attention: Buffer size given as (ADC_BUF_LEN)
	if (HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc_buf, ADC_BUF_LEN) != HAL_OK)
	{
		Error_Handler();
	}

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	// Check to send data
	if (data_ready_flag != 0 && data_to_send_ptr != NULL)
	{
		// Put the sync header to head of buffer
		usb_tx_buf[0] = SYNC_WORD_HIGH;
		usb_tx_buf[1] = SYNC_WORD_LOW;

		// Copy ADC data after sync header
		memcpy(&usb_tx_buf[2], (uint8_t*)data_to_send_ptr, ADC_CHANNEL_COUNT * sizeof(uint16_t));

		// Get ready and send packet
		USBD_StatusTypeDef usb_status;
		uint32_t start_time = HAL_GetTick(); // Add timeout
		do {
			// Size of sending data: TX_PACKET_SIZE
			usb_status = CDC_Transmit_FS(usb_tx_buf, TX_PACKET_SIZE);

			// Timeout check (ex. 100ms) - Do not wait forever if USB not connected
			if (usb_status != USBD_BUSY && HAL_GetTick() - start_time > 100) {
				 // Handle error or timeout
				 break;
			}
		} while (usb_status == USBD_BUSY); // Wait until USB gets ready

		if (usb_status == USBD_OK) {
		  // Transmit completed successfully (or copied to buffer)
		  data_ready_flag = 0; // clear flag
		  data_to_send_ptr = NULL;
		} else {
		   // Error received on transmit (ex. USB not connected), log it or handle error
		}
	}

	// Other works may be done here to prevent 100% CPU wait
	HAL_Delay(1);
  }
  /* USER CODE END 3 */
}

